CREATE TABLE creature
(
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(70) NOT NULL,
    body_shape VARCHAR(255) NOT NULL,
    body_color VARCHAR(255) NOT NULL,
    eye_shape VARCHAR(255) NOT NULL,
    eye_color VARCHAR(255) NOT NULL,
    antenna BOOLEAN NOT NULL,
    horns BOOLEAN NOT NULL,
    tail BOOLEAN NOT NULL,
    ears BOOLEAN NOT NULL,
    proboscis BOOLEAN NOT NULL
);